package Entity;

/**
 *
 * @author Admin
 */
public class Skill {
    private String id;
    private String name;
    private int status;

    /**
     *
     */
    public Skill() {
    }

    /**
     *
     * @param id
     * @param name
     * @param status
     */
    public Skill(String id, String name, int status) {
        this.id = id;
        this.name = name;
        this.status = status;
    }
    
    /**
     *
     * @return
     */
    public String getId() {
        return id;
    }

    /**
     *
     * @param id
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     *
     * @return
     */
    public String getName() {
        return name;
    }

    /**
     *
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     *
     * @return
     */
    public int getStatus() {
        return status;
    }

    /**
     *
     * @param status
     */
    public void setStatus(int status) {
        this.status = status;
    }
}
